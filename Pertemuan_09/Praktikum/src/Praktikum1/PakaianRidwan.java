package Praktikum1;

public class PakaianRidwan {
    String jenis, warna, merk, ukuran;
    double harga;

    PakaianRidwan(String jenis, String warna, String merk, String ukuran, double harga) {
        this.jenis = jenis;
        this.warna = warna;
        this.merk = merk;
        this.ukuran = ukuran;
        this.harga = harga;
    }
}
